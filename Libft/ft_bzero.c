#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*str;
	int		c;

	i = 0;
	str = (char *)s;
	c = '\0';
	if (n != 0)
	{
		while (i < n)
		{
			str[i] = (unsigned char)c;
			i++;
		}
	}
}
